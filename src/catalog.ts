export interface Exhibit {
  readonly id: number;
  readonly variant: number;
  readonly pointCount: number;
  readonly defaultBody: string;
  readonly defaultPulse: string;
  readonly code: string;
}

type Palette = readonly [body: string, pulse: string];

const PALETTES: readonly Palette[] = [
  ['#f5f5f0', '#ff1b12'],
  ['#366fad', '#10ccff'],
  ['#3f8a68', '#46ff8c'],
  ['#8059b0', '#d461ff'],
  ['#986438', '#ff941f'],
  ['#9e4666', '#ff387a'],
  ['#3d68aa', '#4d87ff'],
] as const;

function exhibit(
  variant: number,
  pointCount: number,
  lines: readonly string[],
  palette: Palette = PALETTES[variant % PALETTES.length],
): Exhibit {

  return {
    id: variant,
    variant,
    pointCount,
    defaultBody: palette[0],
    defaultPulse: palette[1],
    code: lines.join('\n'),
  };
}

